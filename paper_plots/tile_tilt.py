# https://math.stackexchange.com/questions/99299/best-fitting-plane-given-a-set-of-points
# https://stackoverflow.com/questions/7997152/python-3d-polynomial-surface-fit-order-dependent/7997925#7997925


def tile_gradint(f, za):

    maps = beam_maps(f)
    res = maps[0][1]

    pixels = np.arange(hp.nside2npix(nside))
    x, y, _ = hp.pix2vec(nside, pixels)

    # Select central section of beam residual to fit
    #  pb = int(hp.ang2pix(32, np.radians(za), 0))
    #  res = res[:pb]
    #  z = res[~np.isnan(res)]
    #  x = x[:pb][~np.isnan(res)]
    #  y = y[:pb][~np.isnan(res)]

    z = res[~np.isnan(res)]
    x = x[~np.isnan(res)]
    y = y[~np.isnan(res)]

    tmp_A = []
    tmp_b = []
    for i in range(len(x)):
        tmp_A.append([x[i], y[i], 1])
        tmp_b.append(z[i])
    b = np.matrix(tmp_b).T
    A = np.matrix(tmp_A)
    fit, residual, rnk, s = lstsq(A, b)

    X, Y = np.meshgrid(np.linspace(-1, 1, 128), np.linspace(-1, 1, 128))
    Z = np.zeros(X.shape)
    for r in range(X.shape[0]):
        for c in range(X.shape[1]):
            Z[r, c] = fit[0] * X[r, c] + fit[1] * Y[r, c] + fit[2]

    fig, ax = plt.subplots(figsize=(9, 7))
    im = ax.imshow(Z, extent=(-1, 1, 1, -1))
    sc = ax.scatter(x, y, c=z, s=7)
    fig.colorbar(im, ax=ax)
    ax.set_title(f"Zenith Angle: {za}")

    plt.tight_layout()
    plt.savefig(f"surf_{za}.png")
    plt.close()


tile_gradint(f"{map_dir}/S06XX_rf0XX_tile_maps.npz", 40)
